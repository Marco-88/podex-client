	<h3>Welcome</h3>
	<p>This is a clone of the JavaScript Sandbox from OpenAI built on Codex.
		You can enter some natural language instructions, and it will turn them into JavaScript.</p>

	<p>For example, if you enter “Count to ten,”
		Codex will generate a function to count to ten and display the numbers 1 through 10.
		You can even create a mini-application by providing a sequence of instructions.</p>

	<h4>Try it out</h4>
	<p>To manipulate an image, try submitting these instructions:</p>

	<p><code>Display this image of a cat: https://bit.ly/3fsc0rH</code></p>

	<p>The application should generate the code to show the image.</p>

	<p><code>Make it 20%</code></p>
	<p>This will reduce the size of the image.</p>

	<p><code>Make the cat black and white</code></p>
	<p>This changes the color.</p>

	<p><code>Make it circular</code></p>
	<p>The image should now be a round avatar.
		You can try other functions like making the image inverted or changing the size or position.
	</p>

